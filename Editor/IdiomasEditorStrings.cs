    private const string PREF_KEY = "Idiomas_EditorLanguage";
    private const string DEFAULT_LANG = "es";

    private static string _cachedLang;

    /// <summary>Idioma actual del editor. Se cachea para no leer EditorPrefs cada frame.</summary>
    public static string CurrentLanguage
    {
        get
        {
            if (_cachedLang == null)
                _cachedLang = EditorPrefs.GetString(PREF_KEY, DEFAULT_LANG);
            return _cachedLang;
        }
        set
        {
            if (_cachedLang == value) return;
            _cachedLang = value;
            EditorPrefs.SetString(PREF_KEY, value);
        }
    }

    /// <summary>Obtiene la cadena traducida para la clave dada en el idioma actual del editor.</summary>
    public static string Get(string key)
    {
        string lang = CurrentLanguage;

        if (_strings.TryGetValue(lang, out var langDict))
        {
            if (langDict.TryGetValue(key, out string val))
                return val;
        }

        // Fallback a español
        if (lang != DEFAULT_LANG && _strings.TryGetValue(DEFAULT_LANG, out var fallback))
        {
            if (fallback.TryGetValue(key, out string val))
                return val;
        }

        // Ultimo recurso: devolver la clave
        return key;
    }

